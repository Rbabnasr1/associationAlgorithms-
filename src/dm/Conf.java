package dm;

/**
 *
 * @author rabab
 */
public class Conf {
    private String x;
    private String y;
    private float con;

    /**
     * @return the x
     */
    public String getX() {
        return x;
    }

    /**
     * @param x the x to set
     */
    public void setX(String x) {
        this.x = x;
    }

    /**
     * @return the y
     */
    public String getY() {
        return y;
    }

    /**
     * @param y the y to set
     */
    public void setY(String y) {
        this.y = y;
    }

    /**
     * @return the con
     */
    public float getCon() {
        return con;
    }

    /**
     * @param con the con to set
     */
    public void setCon(float con) {
        this.con = con;
    }
}
